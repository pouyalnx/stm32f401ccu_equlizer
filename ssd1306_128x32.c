void stdout_goxy_oled(int x,int y){
	page=x;//x%PAGE;
	column=0;//(y*font[2])%COLUMN;
}

void stdout_write_oled(char *data){
	int gpv0,zero_cnt;	
	if(isInitated==false){
		stdout_init_oled();
		isInitated=true;
	}
	i2c_init;
	i2c_start;
	i2c_write(0x80);
	i2c_write(0xb0|(page&0xf));
	i2c_write(0x80);
	i2c_write(column&0xf);
	i2c_write(0x80);
	i2c_write(0x10|((column>>4)&0xf));
	i2c_write(0x40);
	
	zero_cnt=0;
	column+=font[2];
	if((COLUMN-column)<font[2]){
		zero_cnt=COLUMN-column;
		column=0;
		page+=1;
		if(page==PAGE) page=0;
	}
	
	
	for(gpv0=0;gpv0<font[2];gpv0++)  i2c_write(data[gpv0]);
	while(zero_cnt--)					   i2c_write(0);
	i2c_stop(false);
}


int stdout_putchar_oled(int ch){	
	int start_ptr,gpv0,zero_cnt;
	if(!isInitated){
		stdout_init_oled();
		isInitated=true;
	}
	if(ch=='\b'){
		if(column!=0){
			column-=font[2];
			stdout_putchar_oled(' ');
			column-=font[2];
		}
		else{
		   column=(COLUMN/font[2]-1)*font[2];
			if(page==0) page=PAGE-1;
			else			page-=1;
			stdout_putchar_oled(' ');
			column=(COLUMN/font[2]-1)*font[2];
			if(page==0) page=PAGE-1;
			else			page-=1;
		}
		return ch;
	}
	if(ch=='\n' || ch=='\r'){
		start_ptr=page;
		while(start_ptr==page) stdout_putchar_oled(' ');
		return ch;
	}
	if(ch=='\t'){
		zero_cnt=4-(column/font[2])%4;
		while(zero_cnt--) stdout_putchar_oled(' ');
		return ch;
	}
	if(ch<font[4] || ch>(font[4]+font[5])) ch='?';
	
	i2c_init;
	i2c_start;
	i2c_write(0x80);
	i2c_write(0xb0|(page&0xf));
	i2c_write(0x80);
	i2c_write(column&0xf);
	i2c_write(0x80);
	i2c_write(0x10|((column>>4)&0xf));
	i2c_write(0x40);
	
	zero_cnt=0;
	column+=font[2];
	if((COLUMN-column)<font[2]){
		zero_cnt=COLUMN-column;
		column=0;
		page+=1;
		if(page==PAGE) page=0;
	}
	
	
	start_ptr=6+(ch-font[4])*font[2];
	for(gpv0=0;gpv0<font[2];gpv0++)  i2c_write(font[gpv0+start_ptr]);
	while(zero_cnt--)					   i2c_write(0);
	i2c_stop(false);
	
	return ch;
}

